func checkParserErrors(t *testing.T, p *Parser) {
	errors := p.Errors()
	if len(errors) == 0 {
		return
	}
	t.Errorf("parder has %d errors", len(errors))
	for i, msg := range errors {
		t.Errorf("%d) parser error: %q", i+1, msg)
	}
	t.FailNow()
}

